     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($fileId)
    {
        $file = File::firstWhere('id', $fileId);
        $dir = $file->title . ".txt";
        $file_link = fopen($dir, "x");
        $content = $file->content;
        fwrite($file_link, $content);
        fclose($file_link);
    }

